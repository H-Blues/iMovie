export const getMovieReviews = (id) => {
  return fetch(
    baseUrl + `movie/${id}/reviews?api_key=${process.env.REACT_APP_TMDB_KEY}`
  )
    .then((res) => res.json())
    .then((json) => {
      return json.results;
    });
};

